export const sumOfAscii = (str: string): number =>
    [...str].reduce((a, b) => a + b.charCodeAt(0), 0);

export const getColorFromString = (str: string = ""): string => {
    const colorList: string[] = [
        "#99D8CE",
        "#6BC3FE",
        "#5060C1",
        "#835EC3",
        "#B16DBD",
        "#FF85BC",
        "#FF7E83",
        "#A2D959",
        "#59BA5E",
    ];
    return colorList[sumOfAscii(str) % colorList.length];
};
